package model;

/**
 *
 * @author Minh
 */
public class OShow {
    private String title, icon;
    private int price, quantity;

    public OShow() {
    }

    public OShow(String title, String icon, int price, int quantity) {
        this.title = title;
        this.icon = icon;
        this.price = price;
        this.quantity = quantity;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
}
