package model;

/**
 *
 * @author Minh
 */
public class UDetail {
    private int id;
    private String phone, address;
    private int gender;

    public UDetail() {
    }

    public UDetail(int id, String phone, String address, int gender) {
        this.id = id;
        this.phone = phone;
        this.address = address;
        this.gender = gender;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public int getGender() {
        return gender;
    }

    public void setGender(int gender) {
        this.gender = gender;
    }
}
