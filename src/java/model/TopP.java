package model;

/**
 *
 * @author Minh
 */
public class TopP {
    private int id;
    private String title;
    private String icon;
    private int price;

    public TopP() {
    }

    public TopP(int id, String title, String icon, int price) {
        this.id = id;
        this.title = title;
        this.icon = icon;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
